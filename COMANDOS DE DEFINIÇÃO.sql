-- Linguagem de consulta estruturada SQL
-- Padroniza SQL
-- Subconjuto da Linguagem SQL

/* DML - Linguagem de Manipulação de Dados
	UPDATE - usado para alterar dados (atualizar)
	SELECT - usado para selecionar/visualizar dados
	DELETE - usado para deletar dados
	INSERT - usado para inserir dados
*/

/* DDL - Linguagem de Definição de Dados
	ALTER - permite alterar um objeto no banco, seja tabela, colunas
	CREATE - permite criar objetos
	DROP - permite apagar um objeto do banco 
*/

/* DCL - Linguagem de Controle de Dados
	GRANT - concede previlégios
	REVOKE - retira previlégios
*/

/* DTL - Linguagem de Transação de Dados
	BEGIN - marca o início da transação
	COMMIT - encerra uma transação
	ROLLBACK - encerra a transação, desfazendo todas as operações
*/

/* DQL - Linguagem de Consulta de Dados
	SELECT
	FROM - Indica a tabela de onde os dados serão recuperados.
	WHERE - Define os critérios para filtrar os resultados.
	ORDER BY - Ordena os resultados por uma ou mais colunas.
	GROUP BY - Agrupa os resultados por uma ou mais colunas.
	HAVING - Filtra os resultados após o agrupamento.
	DISTINCT - Retorna apenas valores distintos.
	LIMIT - Limita o número de resultados retornados.
	OFFSET - Pula um número específico de linhas antes de retornar os resultados.
*/